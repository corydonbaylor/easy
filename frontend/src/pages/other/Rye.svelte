<h1>Hot Spring Bread</h1>
<p>
  I remember watching a guide take us to the muddy shore of Lake Laugarvatn and
  dig out a enamel pot. He told us that it had been baking there for the past 24
  hours. You see, the Lake Laugarvatn rests on natural geothermal springs. And
  if you find a spot of bubbling soil, that means the spring are hot enough to
  bake bread.
</p>
<p>
  We had our rye bread with smoked salmon. The guide gave us this recipe and
  told us that if we didn&#39;t have a geothermal spring nearby we could use the
  oven at 350 degrees for 1 hour.
</p>
<h2>Ingredients</h2>
<p>4 cups of rye flour</p>
<p>2 cups of regular flour</p>
<p>2 cups of sugar</p>
<p>4 teaspoons baking powder</p>
<p>1 teaspoon salt</p>
<p>4.25 cups of milk</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Mix all the dry ingredients together and then add the
  milk. Mix all the ingredients until a dough forms and then lightly knead.
</p>
<p><strong>Step 2:</strong> Grease a deep dish and add the dough</p>
<p><strong>Step 3:</strong> Cook in a 375 degree oven for 1 hour</p>
