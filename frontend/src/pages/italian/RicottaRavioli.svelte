<h1>Ricotta Ravioli with Sage Butter</h1>
<p>
  Ravioli has been unfairly maligned in my mind by Chef Boyardee. I always think
  of ravioli to be a slimily pouch with mealy, gritty beef covered in ketchup-y
  marinara. No offense to the esteemed chef, but good ravioli can be so much
  more than that. Its best with simple ingredients.
</p>
<p>
  Be forewarned, handmade ravioli is no picnic. It involves some hard work. Save
  this dish for a special occasion.
</p>
<h2>Ingredients</h2>
<p>4 cups flour</p>
<p>4 eggs</p>
<p>8 ounces of Ricotta</p>
<p>1/2 a cup of shredded parmesan</p>
<p>Nutmeg</p>
<p>1 stick of butter</p>
<p>10-12 Sage Leaves</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Put your flour into a nice deep mixing bowl and hollow
  out a well in the center
</p>
<p>
  <strong>Step 2:</strong> Add your eggs to the center of the flour and incorporate
  them with a fork
</p>
<p>
  <strong>Step 3:</strong> Keep adding more and more flour to the mixture until it
  forms a dough. If it is still wet, add more flour. In general, its harder to have
  too much flour because the eggs will incorporate the appropriate amount of dough
  until they &quot;dry out&quot;. You want it to be somewhat firm before you take
  it out of the mixing bowl
</p>
<p>
  <strong>Step 4:</strong> Knead the dough for 7-10 mins, until when you press your
  thumb against it, it springs back. That means that the gluten has formed
</p>
<p>
  <strong>Step 5:</strong> Wrap in plastic wrap and let rest for 30 mins. You now
  need the gluten to relax
</p>
<p>
  <strong>Step 6:</strong> Make the filing. Mix in the ricotta and parmesan. Add
  a few shakes of nutmeg.
</p>
<p>
  <strong>Step 7:</strong> Using a rolling pin, roll out your dough into sheets.
  Cut those sheets into even strips about 2 inches wide. Make sure you have two identical
  strips. You will be placing one strip on top of the other later, in order to encase
  the filing.
</p>
<p>
  <strong>Step 8:</strong> On one strip, place little balls of the filing--no more
  than a tablespoons worth--leaving an inch between each ball. Dip your fingers in
  water and then run them along the exposed dough. This will help the other strip
  of pasta dough stick and fit snuggly on top.
</p>
<p>
  <strong>Step 9:</strong> Place the other strip on top of the first. Press down
  along the dough until little mounds of ricotta form buried between the two strips
  of pasta dough. Try and get as much air out as possible.
</p>
<p>
  <strong>Step 10:</strong> Cut out your ravioli into squares and crimp the ends
  with a fork. Place into boiling water for a minute or two. Until the ravioli floats
  to the top.
</p>
<p>
  <strong>Step 11:</strong> Melt the stick of butter in a sauce pan until it begins
  to foam. Add in the sage leaves and cook for a minute. Top the cooked ravioli with
  the sauce.
</p>
