<h1>Pesto</h1>
<p>
  As a child, I was scared off from pesto due to its bright green color and the
  resulting association with vegetables. Luckily, the older me now understands
  that pesto is as much about parmesan cheese as it is about basil, though that
  is not to say that basil is not delicious.
</p>
<h2>Ingredients</h2>
<p>2 cups of fresh basil leaves</p>
<p>2 tablespoons of crushed walnuts</p>
<p>2 large cloves of garlic</p>
<p>1/2 cup of parmesan cheese</p>
<p>1/2 cup of olive oil</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Combine basil leaves, nuts and garlic in a blender and
  blend
</p>
<p>
  <strong>Step 2:</strong> Adding a small amount of oil at a time, add in olive oil.
  Do this until the desired consistency is reached
</p>
<p>
  <strong>Step 3:</strong> Add the parmesan and pulse the blender until it is combined
</p>
