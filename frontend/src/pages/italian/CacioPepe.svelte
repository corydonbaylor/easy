<h1>Cacio e Pepe</h1>
<p>
  This is adult Mac and cheese. There is no getting around it. It&#39;s creamy
  and simple, unctuous with bite. Its the perfect lunch or dinner to make when
  you have nothing but some flour, eggs, and parmesan cheese. I will give one
  word of warning. Do not skimp on the cheese. I have tried to make this with
  Kraft Pre-shredded Parmesan and the results were horrific. The cheese
  immediately congealed into a clump and never formed a nice sauce.
</p>
<p>
  With this little ingredients, the quality matters greatly. Choose real, <em
    >unshredded</em
  > parmesan reggiano and pecorino romano and you will be in for a treat. This is
  unfortunately one of those recipes that you do by feel rather than follow directly,
  so all ingredient amounts are meant as guidance rather than gospel.
</p>
<h2>Ingredients</h2>
<p>1 block of parmesan reggiano</p>
<p>1 block of pecorino romano</p>
<p>1.5 cups of flour</p>
<p>3 eggs</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Put your flour into a nice deep mixing bowl and hollow
  out a well in the center
</p>
<p>
  <strong>Step 2:</strong> Add your eggs to the center of the flour and incorporate
  them with a fork
</p>
<p>
  <strong>Step 3:</strong> Keep adding more and more flour to the mixture until it
  forms a dough. If it is still wet, add more flour. In general, its harder to have
  too much flour because the eggs will incorporate the appropriate amount of dough
  until they &quot;dry out&quot;. You want it to be somewhat firm before you take
  it out of the mixing bowl
</p>
<p>
  <strong>Step 4:</strong> Knead the dough for 7-10 mins, until when you press your
  thumb against it, it springs back. That means that the gluten has formed
</p>
<p>
  <strong>Step 5:</strong> Wrap in plastic wrap and let rest for 30 mins. You now
  need the gluten to relax
</p>
<p>
  <strong>Step 6:</strong> Using a rolling pin, roll out your dough into sheets.
  Then using a pasta maker, roll it out further and cut it. You can use a knife if
  you don&#39;t have a pasta machine.
</p>
<p>
  <strong>Step 7:</strong> Boil the pasta in lightly salted water for about 5 minutes
</p>
<p>
  <strong>Step 8:</strong> While the pasta is boiling, shred a mix of parmesan reggiano
  and pecorino romano into a serving tray or a bowl. You want maybe a half an inch
  of cheese at the bottom of the tray or bowl.
</p>
<p>
  <strong>Step 9:</strong> Without draining the pasta, using a slotted spoon or tongs,
  place your noodles into the bowl with cheese. Mix it up. The pasta water that clings
  to the noodles should mix with cheese to form a sauce. If the cheese remains too
  dry, add some more pasta water, a tablespoon at a time
</p>
