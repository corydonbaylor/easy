<h1>Mákosguba</h1>
<p>
  In the summer of 2019, my mother and I went on a trip together to Hungary. We
  took a class called &quot;Easy Cooking Budapest&quot; and was led by a woman
  called Cecilia Kertész. Between chopping vegetables and cooking meat, she
  would tell us how times were tough and how under communist rule at least
  everyone had what they needed.
</p>
<p>
  The mákosguba is originally a Christmas dessert for good luck. It is said,
  that the many little poppy seeds will bring wealth to the house in the new
  year.
</p>
<h2>Ingredients:</h2>
<p>15 tablespoons of ground poppy seeds</p>
<p>7 tablespoons of powdered sugar</p>
<p>10 dry crescent rolls (you can use dry white bread or sweet bread)</p>
<p>1,5 liters of milk (2,8 or 3,5% fat)</p>
<p>1 tablespoon of rum (or rum aroma)</p>
<p>Raisins (optional)</p>
<p>1 pack of vanilla sugar</p>
<p><strong>For the vanilla custard:</strong></p>
<p>5 egg yolks</p>
<p>4 tablespoons of sugar</p>
<p>1-2 tablespoons of flour</p>
<p>0,5 liters of milk</p>
<p>1 pack of vanilla sugar</p>
<h2>Instructions:</h2>
<p><strong>Step 1:</strong> Cut the crescent rolls in 2 cm wide slices.</p>
<p><strong>Step 2:</strong> Boil the milk with the pack of vanilla sugar.</p>
<p>
  <strong>Step 3:</strong> Pour the milk on the crescent rolls, make sure all of
  them are soaked.
</p>
<p><strong>Step 4:</strong> Mix the poppy seeds with the sugar.</p>
<p>
  <strong>Step 5:</strong> Get a big bowl that you can also put in the oven. Put
  a layer of the soaked bread rolls on the bottom, then a layer of the poppy seed-
  sugar mix, then a layer of the bread again. Carry on until you run out of ingredients.
  You can also put raisins between the layers.
</p>
<p>
  <strong>Step 6: </strong> Put the bowl in the oven for 10-15 minutes on 150 °C.
</p>
<p><em>Prepare the vanilla custard:</em></p>
<p>
  <strong>Step 1:</strong> Mix the egg yolks with the sugar and the vanilla sugar.
</p>
<p>
  <strong>Step 2:</strong> Slowly add the flour and the milk to the egg yolks over
  gentle heat until it is as thick as you want it.
</p>
<p>
  <strong>Step 3:</strong> Serve the baked poppy seed bread with the vanilla custard.
  You can use sweet butter instead of the vanilla custard: heat up butter in a pan
  until just before it starts burning.
</p>
