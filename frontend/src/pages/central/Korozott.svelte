<h1><strong>Kőrözött</strong></h1>
<p>
  In the summer of 2019, my mother and I went on a trip together to Hungary. We
  took a class called &quot;Easy Cooking Budapest&quot; and was led by a woman
  called Cecilia Kertész. Between chopping vegetables and cooking meat, she
  would tell us how times were tough and how under communist rule at least
  everyone had what they needed. Below is her recipe for Kőrözött which is a
  sort of dip that is best served with chips or toasted bread.
</p>
<p><strong>Ingredients:</strong></p>
<p>
  400 g (14 oz) sheep cottage cheese (cow cheese for a milder taste, or 50-50%) <em
    >Use ricotta if not available</em
  >
</p>
<p>1 onion</p>
<p>175 g (6 oz) sour cream (or more if you want it creamier)</p>
<p>4 teaspoons of paprika powder</p>
<p>2 teaspoons of ground pepper</p>
<p>2 tablespoons of mustard</p>
<p>2 fresh spring onions (optional)</p>
<p>1 teaspoon of hot paprika/ chili pepper</p>
<p>1/2 teaspoon ground caraway seed</p>
<p>1 coffee-spoon of cognac (optional)</p>
<h2>Instructions</h2>
<p><strong>Step 1:</strong> Chop the onions in small pieces.</p>
<p><strong>Step 2:</strong> Mix the onions in the ricotta.</p>
<p><strong>Step 3:</strong> Then, mix in the paprika powder.</p>
<p>
  <strong>Step 4:</strong> Mix in the other spices, the mustard and the cognac. (You
  don’t need chili if the paprika powder is hot.)
</p>
<p>
  <strong>Step 5:</strong> Mix in the sour cream with a fork, the amount depends
  on how creamy you like your kőrözött.
</p>
