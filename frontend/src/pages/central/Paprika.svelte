<h1>Chicken Paprikás with Nokedl</h1>
<p>
  In the summer of 2019, my mother and I went on a trip together to Hungary. We
  took a class called &quot;Easy Cooking Budapest&quot; and was led by a woman
  called Cecilia Kertész. Between chopping vegetables and cooking meat, she
  would tell us how times were tough and how under communist rule at least
  everyone had what they needed. Below is her recipe for Chicken Paprikas with
  Nokedl, a traditional Hungarian noodle.
</p>
<p>Cooked meat, onions and paprika. Nothing could be more Hungarian!</p>
<h2><strong>Ingredients:</strong></h2>
<p>2 big onions</p>
<p>Approx. 4 tablespoons of sunflower oil</p>
<p>4 teaspoons of salt</p>
<p>1 teaspoon of ground pepper</p>
<p>5 teaspoons of paprika powder</p>
<p>6 legs of chicken</p>
<p>330 g (12 oz) sour cream (20% fat)</p>
<p>1 tablespoon of flour</p>
<p>Water</p>
<p><strong>Nokedl</strong>:</p>
<p>9 tablespoons of flour</p>
<p>3 eggs</p>
<p>60 ml (0,25 cups) water - optional</p>
<p>1 coffee spoon of salt</p>
<h2>Instructions:</h2>
<p>
  <strong>Step 1: </strong>Chop the onions into small pieces. Heat the oil in a
  big pan and fry the onions until they are glassy.
</p>
<p>
  <strong>Step 2: </strong>Pull the pan off the fire and add the salt, pepper
  and paprika powder. Pour 1 dl (half a cup) of water on the mixture, so the
  paprika powder doesn’t get burnt, then put the pan back over the fire.
</p>
<p>
  <strong>Step 3</strong>: Wash the chicken legs and cut them into upper and
  lower leg (thigh and leg). Add the chicken legs to the pan and pour water on
  top, just to cover them up.
</p>
<p>
  <strong>Step 4:</strong> Cover the pan and cook for one hour on high heat.
  <em
    >The goal here is to braise the legs. You can also put them in the oven at
    350 for an hour</em
  >
</p>
<p><strong>Step 4:</strong> Mix the sour cream with the flour.</p>
<p>
  <strong>Step 5:</strong> Once the chicken is soft, pull it off the fire and mix
  in the sour cream. Boil once more.
</p>
<p><strong>Prepare the nokedli:</strong></p>
<p><em>I would suggest doing this while the chicken is braising</em></p>
<p><strong>Step 1:</strong> Mix the ingredients until you get a hard dough.</p>
<p>
  <strong>Step 2:</strong> ’Tear’ the nokedli-dough into boiling water. Keep it on
  the fire until it boils again (don’t cook any longer).
</p>
<p><strong>Step 3:</strong> Serve the chicken paprikás with the nokedli.</p>
