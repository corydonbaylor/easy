<h1>Hungarian Pea Soup</h1>
<p>
  In the summer of 2019, my mother and I went on a trip together to Hungary. We
  took a class called &quot;Easy Cooking Budapest&quot; and was led by a woman
  called Cecilia Kertész. Between chopping vegetables and cooking meat, she
  would tell us how times were tough and how under communist rule at least
  everyone had what they needed. Below is her recipe for a traditional pea soup.
</p>
<p>This is the vegetarian version, but it is great with chicken too.</p>
<h2><strong>Ingredients:</strong></h2>
<p><strong>For the soup</strong></p>
<p>3 tablespoons of cooking oil</p>
<p>1 big onion</p>
<p>18 oz (400 g) of green pea (fresh or frozen)</p>
<p>1 tablespoon of tarragon</p>
<p>3 carrots</p>
<p>1 parsley root or turnip</p>
<p>2 liters of vegetable broth</p>
<p>1 teaspoon of salt</p>
<p>1 coffee spoon of ground black pepper</p>
<p>Parsley</p>
<p>3 dl (10 oz) cooking cream</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Chop the onion into small pieces and fry it on hot oil,
  until it is glassy.
</p>
<p><strong>Step 2:</strong> Add the tarragon, salt and pepper.</p>
<p>
  <strong>Step 3:</strong> Clean the vegetables and cut them into fine slices. Put
  the vegetables together with the pea on the onion (if the pea is very tender, 7-8
  minutes of cooking is enough, so add only 20 minutes later, otherwise it will get
  hard and loose its colour).
</p>
<p>
  <strong>Step 4:</strong> Fry the vegetables for 3-5 minutes, then pour the broth
  on the mixture and cook ready for 30 minutes.
</p>
<p>
  <strong>Step 5:</strong> Add hot broth to the cream to heat it up before you add
  it to the soup, then slowly mix it into the soup.iKeep it on the fire until it
  boils again (don’t cook any longer).
</p>
<p>
  <strong>Step 6:</strong> Before serving, sprinkle some freshly chopped parsley
  on top.
</p>
