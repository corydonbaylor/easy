<h1>Buttermilk Biscuits</h1>
<p>
  Every Tuesday morning my cafeteria would make biscuits and sausage gravy.
  Objectively, it was not good. The biscuits were often times hard and crunchy,
  instead of soft and fluffy. Its hard to mess up sausage gravy though. I would
  always make little biscuits and gravy sandwiches stuffed with eggs and bacon.
</p>
<p>Here&#39;s how to make biscuits that live up to my memory of them.</p>
<h2>Ingredients</h2>
<p><strong>Dry</strong></p>
<p>1.5 cups of flour</p>
<p>2 tablespoons of sugar</p>
<p>1 tablespoon of baking powder</p>
<p>1/2 teaspoon of baking soda</p>
<p>Salt</p>
<p><strong>Wet</strong></p>
<p>1.5 sticks of frozen butter (6 oz)</p>
<p>1 1/4 cups of buttermilk (normal milk will work too)</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Combine all the dry ingredients together in a bowel and
  whisk together well
</p>
<p>
  <strong>Step 2:</strong> Grate the two frozen sticks of butter into your bowel
  of dry ingredients and mix so that all the butter is coated. Its best to wear plastic
  gloves at this point. You want the butter to remain cold.
</p>
<p>
  <strong>Step 3:</strong> Make a little hole in the center of your dries and add
  the buttermilk. Using a fork, work in the flour into the buttermilk. It will form
  a flaky dough.
</p>
<p>
  <strong>Step 4:</strong> Roll out the dough into a sheet. Fold the sheet over itself,
  like you are folding a sheet of paper. Roll it out again, and then fold it over
  again. This is called lamination. You want to create flaky layers of dough here.
  But don&#39;t work the dough too hard. You want to prevent gluten development.
  Gluten results in chewy tough biscuits. You don&#39;t want chewy tough biscuits.
</p>
<p>
  <strong>Step 5:</strong> Put your dough into the fridge for 30 mins, again to harden
  up the butter. Preheat the oven to 400 degrees.
</p>
<p>
  <strong>Step 6:</strong> Roll out your dough to an inch or two thickness and put
  it in the oven for 25-30 minutes
</p>
