<h1>Grits</h1>
<p>
  Decades ago, grandmother Sandra made a solemn oath to never distribute her
  famous grits recipe to the various old southern ladies that reside in Virginia
  Beach. She managed to get the below recipe from a caterer who asked that she
  keep it secret, lest his grits become common in the area. True to her word, it
  took literal decades to pry out this recipe. She gave us quite a few red
  herrings, each time changing the ingredients so that we never quite got the
  right thing. Eventually, my sister Allie snagged the recipe by taking a
  picture of it when she wasn&#39;t looking.
</p>
<h2>Ingredients</h2>
<p>1 cup of Gruyere cheese, shredded</p>
<p>1 quart of milk</p>
<p>1/2 cup of butter and 1/3 cup of butter</p>
<p>1/2 cup of Parmesan cheese, shredded</p>
<p>1 cup of slow cooked grits</p>
<p>1 teaspoon of salt</p>
<p>1/4 teaspoon of white pepper</p>
<h1>Instructions</h1>
<p>
  <strong>Step 1:</strong> Skald milk and add butter. Once the butter is melted,
  stir in grits. Resume cooking until grits are the consistency of a hot cereal like
  Cream of Wheat. Remove from heat.
</p>
<p>
  <strong>Step 2:</strong> Stir in shredded cheeses (Gruyere and Parmesan), salt,
  and white pepper.
</p>
<p>
  <strong>Step 3:</strong> Spray casserole dish with cooking oil and pour grits into
  the dish. Top with a little additional Parmesan shredded cheese. Refrigerate until
  you are ready to prepare the dinner. (These grits can be prepared a day or two
  before the meal is served.)
</p>
<p>
  <strong>Step 4:</strong> Bake in the oven at 350 degrees for 30 minutes to 40 minutes
  or until top is golden
</p>
