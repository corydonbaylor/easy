<h1>Sweet Potato Soup</h1>
<p>
  Good comfort food is warm and filing. Great comfort food is also good for you.
  Sweet potato soup is great comfort food. This recipe is easy, taking a little
  less than an hour, with most of that time spent waiting for veggies to cook.
</p>
<p>
  With this recipe, you will make a full pot of soup, providing four servings. I
  think this is best served with good bread, such as a quarter of a baguette.
</p>
<h2>Ingredients</h2>
<p><strong>Veggies</strong></p>
<p>1 sweet potato</p>
<p>1/2 a pound of carrots</p>
<p>1 small white onion</p>
<p>1/2 of a fuji/honeycrisp apple</p>
<p><strong>Spices</strong></p>
<p>3 teaspoons of curry</p>
<p><strong>Other</strong></p>
<p>4 cups of chicken broth (this is one Swanson package)</p>
<p>1 tablespoon of honey</p>
<p>4 tablespoons of butter</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Cook the onions in the bottom of a medium size pot with
  4 tablespoons of butter
</p>
<p>
  <strong>Step 2:</strong> Once the onions are translucent, add the curry powder.
  Stir it up!
</p>
<p><strong>Step 3:</strong> Add chicken broth and bring to a boil</p>
<p>
  <strong>Step 4:</strong> While the chicken broth is coming to a boil, peel and
  cut the sweet potato and carrots into cubes
</p>
<p>
  <strong>Step 5:</strong> Add the sweet potato and carrot, cover and boil for
  around <strong>20 minutes</strong>
</p>
<p>
  <strong>Step 6:</strong> Peel the apple and cut half of it into cubes. After the
  potato and carrots have softened, which again takes about 20 minutes, add the apple
  in. Cook with the apple for about 1 or 2 minutes.
</p>
<p>
  <strong>Step 7: </strong> Using a stick blender, blend the soup until there are
  no more chunks
</p>
