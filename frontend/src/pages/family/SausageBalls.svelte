<h1>Sausage Balls</h1>
<p>
  In our house, these sausage balls have been a holiday favorite for over thirty
  years. If I’m feeling especially merry, I devote a full week to making frozen
  tins of them for friends. It’s a welcome gift that kick starts a party for
  families who are busy, busy, busy.
</p>
<p>-- Audrey Baylor</p>
<h2>Ingredients</h2>
<p>2 pounds of Jimmy Dean hot sausage</p>
<p>2 pounds of Jimmy Dean sage sausage</p>
<p>8 cups of Bisquick (or 1 40 oz box)</p>
<p>
  4 pounds of Vermont Extra Sharp White Cheddar Cheese (or 2 large blocks or 12
  cups, grated)
</p>
<h2>Instructions</h2>
<p><strong>Temperature</strong> 350 degrees</p>
<p>
  <strong>Time in the</strong> <strong>oven</strong> 7-10 minutes, freeze, and then
  another 10 minutes
</p>
<p>
  <strong>Yield</strong> 140 sausage balls (I usually make 5 batches for the holidays
  as gifts for friends and family)
</p>
<p>
  <strong>Step 1:</strong> Throw all of the ingredients in a large mixing bowl. If
  you have a food processor, you’re set to mix, but if not, pull on a pair of disposable
  gloves and mix with your hands until everything is well blended.
</p>
<p>
  <strong>Step 2:</strong> An important point to know out of the gate: Never buy
  bags of grated cheese. There’s nothing better than Vermont Extra Sharp White Cheddar
  and nothing more mediocre than bagged grated cheese. I prefer to buy Vermont Extra
  Sharp Cheddar in big blocks from Costco, or, surprisingly enough, the Private Selection
  blocks of it from Kroger. If either of these are sold out, I use Cracker Barrel
  Extra Sharp Cheddar.
</p>
<p>
  <strong>Step 3:</strong> Roll the sausage dough into cookie-sized balls and half
  bake them, probably between 7 to 10 minutes. Allow them to fully cool before placing
  them in freezer bags and popping them in the freezer. You can package them at this
  point if you want to give them as frozen treats.
</p>
<p>
  <strong>Step 4:</strong> When you’re ready to serve them, do not thaw before placing
  them back on a cookie sheet to brown in the oven for another ten minutes. Serve
  piping hot.
</p>
<p>
  <strong>Step 5:</strong> For Christmas-size batches (for gift-giving and holiday
  parties), the proportions are giant size: 4 sausage packages, 1 box of Biscquick
  (40 oz.), and 12 cups of grated cheese. Each batch yields 5-6 storage containers
  (4 cup size). I usually make this two or three times to have enough to share.
</p>
