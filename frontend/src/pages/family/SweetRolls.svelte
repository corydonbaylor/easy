<h1>Sweet Rolls</h1>
<p>
  Mother baked sweet rolls every Saturday—dozens and dozens of sweet rolls. She
  baked them for Sunday morning, but since the rolls taste best fresh out of the
  oven, we always ate as many as we liked hot out of the oven from her Saturday
  afternoon baking. At about four o’clock on Saturday, mother would head out the
  door to deliver pans of rolls to those she thought might need cheering up:
  nursing home residents, people who lived alone, neighbors. That tradition of
  giving sweet rolls continued into her late eighties and extended to her
  grandchildren, sending home pans of cinnamon treats after nearly every visit.
  Wherever she lived, she baked sweet rolls, and in her last home, one of her
  neighbors regularly brought her a dozen red roses because, he said, she kept
  everyone on their street happy with generous pans of sweet rolls.
</p>
<p>-- Audrey Baylor</p>
<p>&nbsp;</p>
<h2>Ingredients</h2>
<p><strong>Dough</strong></p>
<p>4 cups of milk</p>
<p>1 cup of sugar</p>
<p>2 teaspoons of salt</p>
<p>1 cup or 8 Tablespoons of butter</p>
<p>12 cups of flour</p>
<p>4 eggs</p>
<p>6 packages of Instant Yeast (premium Redstar recommended)</p>
<p><strong>Topping</strong></p>
<p>1 cup or 8 Tablespoons of butter</p>
<p>Sugar</p>
<p>Cinnamon</p>
<p>&nbsp;</p>
<p><strong>Yield:</strong> 2 9x12 pans plus one 9” cake pan of rolls</p>
<p><strong>Bake:</strong> 350 degrees for 17-20 minutes</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Because you are working with yeast, temperatures and assembly
  of ingredients must be followed exactly.
</p>
<p>
  <strong>Step 2:</strong> Skald the milk on the stove (heat at medium heat until
  a skin forms) as you stir to dissolve the sugar, butter, and salt, allowing the
  butter to melt. Do not allow to boil.
</p>
<p>
  <strong>Step 3:</strong> Pour the milk mixture into a very large bowl. My mother
  always had a gigantic bowl that she used just for mixing up sweet rolls. (If you
  do not have a gigantic bowl, you may need to make a half-size batch.) While the
  milk is still hot, add half of the flour (6 cups), stirring thoroughly before adding
  the eggs. You want the mixture to be warm (but not hot) because you need to add
  the yeast and the temperature matters. The yeast is activated by the warm temperature,
  but a hot temperature will kill the yeast and keep your dough from rising. Once
  the yeast is added and stirred in, add the second half of your flour (another 6
  cups). As you are adding the second half of the flour, you will want to work the
  flour into the mixture with your hands. Gloves make this task easy. Do not overwork
  the dough.
</p>
<p>
  <strong>Step 4:</strong> Divide the dough into two parts. Generously sprinkle your
  work surface with flour, probably a cup or maybe even a cup and a half. The dough
  is still wet to the touch so the added flour on the surface should keep your dough
  from sticking. Sprinkle a little additional flour to the top of the dough if you
  see sticky spots that need it.
</p>
<p>
  <strong>Step 5:</strong> Using a rolling pin, roll out the dough into a rectangular
  shape, maybe 24”x12”. The dough should be between ¼ to ½ inch thick. Spread about
  a half stick of softened butter across the dough. Add a generous layer of sugar,
  and a heavy layer of cinnamon upon the buttered dough. Roll tightly into a long
  role. Use a thread to cut the dough into ½ inch wide slices (slide the thread under
  the roll, cross the two ends of the thread and pull for a clean, perfect cut).
  Repeat this process with the second half of your dough.
</p>
<p>
  <strong>Step 6:</strong> Use cooking spray to coat your baking pans. Place the
  rolls so that they are touching each other in the 9x12 cake pans. For nice, high
  sweet rolls, allow them to rise until at or slightly above the rim of the pan.
  How quickly they do this will depend upon the temperature of your room, varying
  from perhaps 40 minutes to a little over an hour. Bake in a preheated 350 degree
  oven from 17 to 20 minutes, depending upon your pan. Dark pans tend to bake more
  quickly and glass pans more slowly. The rolls should appear golden. The rolls will
  continue to darken slightly even after they have been removed from the oven.
</p>
<p>
  <strong>Step 7:</strong> The rolls are best served immediately from the oven although
  they are good warmed in the oven later. You can eat them topped with a bit of butter
  or spread with icing. We never measured the ingredients for the icing, but I’m
  guessing that these approximate guessed measurements will at least get you started:
  2 tablespoons of softened butter, 1 ½ teaspoons of vanilla, 2 cups of confectioner
  sugar, and 1 ½ to 2 Tablespoons of milk.
</p>
<p>&nbsp;</p>
<p><em>Mother also made several variations to her basic recipe.</em></p>
<p><strong>Pecan Sticky Rolls:</strong></p>
<p>
  Spray a round glass baking dish with cooking oil, dot it with butter (probably
  about 4 Tablespoons), add ¾ to a cup of brown sugar, pour in 1/3 cup of Half
  and Half cream before placing the sweet rolls on top of this mixture. These
  measurements are approximate and may need adjustment
</p>
<p><strong>Orange Rolls:</strong></p>
<p>
  Make an orange icing with orange zest (grated orange peel) and orange juice
  concentrate (from the frozen foods section of the grocery store). Stir
  together
</p>
<p>
  2 tablespoons of frozen orange juice concentrate, 2 tablespoons of softened
  butter, ½ teaspoons of vanilla, 3 1/2 cups of confectioner sugar. Measurements
  are approximate and may need to be adjusted. Instead of using sugar and
  cinnamon as the sweet roll filling as is more common, spread the dough with
  this orange icing before rolling up the dough and slicing for the pan. Because
  this variation is more of a treat, Mother generally cut these orange rolls
  into a tea ring. A tea ring is formed by taking the long roll of dough,
  forming a circle with it, and then half cutting each slice and laying it on
  it’s side.
</p>
