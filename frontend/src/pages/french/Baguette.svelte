<h1>French Baguette</h1>
<p>
  Making a French baguette is about setting a schedule, walking over to a bowl
  with weird sticky dough, flopping it over and then popping that into the oven.
  It&#39;s kind of like cooking but feels more like babysitting.
</p>
<p>
  The end result is fresh bread that tastes like the platonic ideal of bread.
  It&#39;s beautiful, fragrant, and makes something that is usually an
  afterthought, the main attraction.
</p>
<h2>Ingredients</h2>
<p>1.5 cups of 100 degree water</p>
<p>2 cups of flour</p>
<p>1 packet of instant yeast</p>
<p>Salt</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Proof your yeast by adding a packet of instant yeast and
  a spoonful of flour to lukewarm (100 degrees) water. Give it five minutes. Some
  bubbles should start to form at the top. This indicates that the yeast is in fact
  alive
</p>
<p>
  <strong>Step 2:</strong> Add two cups of flour and a generous dash of salt to a
  greased, very large bowl. I like to use my dutch oven for this since I can use
  the lid instead of plastic wrap
</p>
<p>
  <strong>Step 3:</strong> Mix the flour, yeast and warm water together. This is
  a high hydration dough so it will be <strong>very</strong> sticky. Cover the dough
  and let rest for 15 minutes
</p>
<p>
  <strong>Step 4:</strong> We will be kneading the dough using something called
  the stretch and fold method. This method takes <em>a lot</em> longer but is also
  much easier. To stretch and fold, grab one corner of the dough and pull it as high
  as it will go without breaking, then fold it over top of the rest of the dough.
  It is as if you are draping it over itself. Do this five or six times, rotating
  the dough each time. After you have done this, flip the dough over and let rest
  for 25 minutes.
</p>
<p>
  <strong>Step 5-8:</strong> Repeat this three to four more time until a gluten network
  has formed. After each time you let the bread rest, you should be able to pull
  the dough farther and farther. You can test if the gluten is strong enough by cutting
  off a piece of the dough and stretching it out in your hands. You will want it
  to be thin enough that it forms a &quot;gluten window&quot;.
</p>
<p>
  <strong>Step 9:</strong> Flour your workspace and divide your dough into two or
  three equal parts. Stretch out your dough into rectangles. Folding lengthwise,
  fold the dough in half and pinch off to seal the dough. Fold in half again lengthwise
  and seal off the dough. This should create a log of dough. Place these on parchment
  paper.
</p>
<p>
  <strong>Step 10:</strong> Let the dough rest for 1 hour or until it becomes 1.5x
  its size. When there is about 20 minutes left in the final rest, turn on the oven
  to 475 degrees. Put your baking sheet in the middle rack and a casserole dish of
  water on the bottom rack.
</p>
<p>
  <strong>Step 11:</strong> After the dough has risen, score the bread by running
  a sharp knife across the side of the bread at a 45 degree angle. All the hot air
  and gas will escape through this opening. Its called the oven spring and it will
  provide a lot of the verticality of the bread. Spritz the bread with water.
</p>
<p>
  <strong>Step 12:</strong> Slide the bread into the oven and cook for 20-30 minutes
  or until the crust is golden brown.
</p>
