<h1>Coq Au Vin</h1>
<p>
  One of my favorite things to do is to buy a whole chicken from the supermarket
  and break it down into the two breasts, two legs, and two thighs. I use the
  rest to make stock. This leads me to need a chicken breast and chicken thigh
  meal for each week. The chicken breast meal is usually pretty straight
  forward. The thigh meals are more interesting.
</p>
<p>
  Due to the higher fat content of the thighs, it stands up well to a braise.
  Coq Au Vin is a French flavored braise of the chicken thighs. Its rich,
  unctuous and quite easy to make.
</p>
<h2>Ingredients</h2>
<p>2 chicken legs</p>
<p>2 chicken thighs</p>
<p>1 yellow onion</p>
<p>4 strips of bacon</p>
<p>3 carrots</p>
<p>1/2 pound of mushrooms</p>
<p>Tomato paste</p>
<p>Minced garlic</p>
<p>1 cup of wine</p>
<p>1 cup of chicken stock</p>
<h2>Instructions</h2>
<p><strong>Step 1: </strong> Marinate the chicken in wine and chicken stock</p>
<p>
  <strong>Step 2:</strong> Cut the bacon into bits and fry in a cast iron pan. Reserve
  the bacon on the side (I often cook this without bacon)
</p>
<p>
  <strong>Step 3:</strong> Brown the chicken in the cast iron. The cast iron should
  be pretty hot to get a good sear. You will finish in the oven, so don&#39;t worry
  about getting everything cooked. Reserve the wine from the marinade.
</p>
<p>
  <strong>Step 4:</strong> Remove the chicken and cook a diced onion and garlic.
  I would suggest waiting a second before tossing in the onion and garlic. While
  the chicken needs high heat to get a proper sear, the onion and especially garlic
  will burn if the iron is too hot
</p>
<p>
  <strong>Step 5:</strong> Add back the wine, the chicken and the bacon to the pan
  with cooked onions. Add mushrooms and carrots. Place into a 350 degree oven for
  1 hour
</p>
