<h1>Cotelette de Porc</h1>
<p>
  The simple pork chop, long a favorite with American households, soon to be
  elevated the French way. Growing up, I would oftentimes have pork chops with
  apple sauce. Something just works about the deep fatty flavor of pork paired
  with the sweet tang of apple sauce. This easy, one-pan recipe makes an elegant
  play on that classic combo.
</p>
<p>
  Mimi Thorisson, who from which this dish is adapted, tells us that most of the
  flavors in this dish--the cream, the apples, the sage--are from the cool,
  rainy region of Normandy. Imagine yourself in Northern France sitting down to
  dinner in a cottage that overlooks your apple orchard. Enjoy!
</p>
<h2>Ingredients</h2>
<p>Bone-In Pork Chop</p>
<p>Butter</p>
<p>4 Shallots</p>
<p>2 Cloves of Garlic</p>
<p>Sage</p>
<p>2/3 a Cup of Apple Cider</p>
<p>3 Tablespoons of Creme Fraiche</p>
<h2>Instructions</h2>
<p><strong>Step 1:</strong> Preheat Oven to 375</p>
<p>
  <strong>Step 2:</strong> Score the pork chops and season with salt and pepper
</p>
<p>
  <strong>Step 3:</strong> Heat butter in the pan and add chopped shallot and crushed
  garlic
</p>
<p>
  <strong>Step 4:</strong> Cook the pork chop on each side for about 5 minutes. The
  temp of the pork chop should reach around 120 degrees
</p>
<p>
  <strong>Step 5:</strong> Place sage leaves and a pad of butter on top of each chop
  and transfer to the oven. It will only need to be in there for about 10 minutes
  to reach an internal temp of 145 degrees.
</p>
<p>
  <strong>Step 6:</strong> Remove pork chops and let rest. In the same pan, add the
  apple cider and let simmer until reduced (about 2 minutes). Add creme fraiche,
  spoonful by spoonful, into the reduced cider, until you have achieved the creamy
  flavor and texture you desire.
</p>
