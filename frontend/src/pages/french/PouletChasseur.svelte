<h1>Poulet Chasseur</h1>
<p>
  Hunters chicken is the lesser known but refined, almost understated brother of
  Coq Au Vin. Whereas Coq Au Vin get all the girls and is captain on the
  football team, Poulet Chasseur was earning straight As and was a tough out on
  the tennis team. Enough with the metaphors, Poulet Chasseur or Hunters Chicken
  is another way to cook the dark meat of the chicken. Using white wine instead
  of red, Hunters Chicken has a softer, more delicate flavor, but is just as
  good.
</p>
<h2>Ingredients</h2>
<p>2 chicken legs</p>
<p>2 chicken thighs</p>
<p>Butter</p>
<p>1 large onion</p>
<p>2 shallots</p>
<p>2 garlic cloves</p>
<p>Mushrooms</p>
<p>1/4 a cup of white wine</p>
<p>3/4 chicken stock</p>
<p>Fresh tarragon</p>
<h2>Instructions</h2>
<p><strong>Step 1:</strong> Preheat oven to 400 degrees</p>
<p>
  <strong>Step 2: </strong> In a very hot cast iron pan, brown chicken legs and thighs.
  The legs may stick at first but will release once they are properly browned.
</p>
<p>
  <strong>Step 3:</strong> Remove chicken and add in onion, garlic, and shallots,
  cook until they are translucent
</p>
<p>
  <strong>Step 4:</strong> Next, add the chicken back to the pan, add the wine and
  chicken stock and cook in the oven for 25-30 minutes
</p>
<p>
  <strong>Step 5:</strong> After taking the pan out of the oven, use a sieve to separate
  the onions and meat from the braising fluid. In the now empty pan, brown the mushrooms
  in butter.
</p>
<p>
  <strong>Step 6:</strong> Once the mushrooms are browned, add a table spoon of flour
  to the butter and mushrooms and pour a cup of the braising fluid on top. Stir until
  it forms a sauce.
</p>
<p>
  <strong>Step 7:</strong> Cover the resting chicken with the sauce and top with
  tarragon
</p>
