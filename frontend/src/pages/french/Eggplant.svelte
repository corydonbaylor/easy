<h1>Roasted Eggplant</h1>
<p>
  A simple side that goes well with any protein heavy dish. I adapted this
  recipe from Mimi Thorisson&#39;s French Country Cooking and interestingly she
  describes this dish as being Italian in nature. But here&#39;s the thing, it
  came from a <em>French</em> cookbook so its going in the <em>French</em> section
  of my website.
</p>
<h2>Ingredients</h2>
<p>1 Large Eggplant</p>
<p>Pine Nuts or Cashews</p>
<p>Raisins</p>
<h2>Instructions</h2>
<p><strong>Step 1:</strong> Preheat the oven to 400 degrees</p>
<p>
  <strong>Step 2:</strong> In a large pan, heat olive oil in medium high heat and
  cook the eggplant until they get a nice golden brown, season with salt and pepper
</p>
<p>
  <strong>Step 3:</strong> Arrange the eggplant in a single layer on the baking sheet,
  scattering the nuts and raisins on top. Roast for about 10 minutes
</p>
