<h1>French Onion Soup</h1>
<p>
  The perfect mix of cheese, bread, and sweet onion taste, French onion soup is
  a modern day classic.It is also perfect for the last few days of the week when
  you are largely out of ingredients. As long as you have onions, beef broth,
  bread, and white cheese, you can make some version of this soup. If you
  don&#39;t have these things or just a few of them, this soup will be
  tantalizingly just out of reach, mocking you for your insolence.
</p>
<h2>Ingredients</h2>
<p>1/2 cup of unsalted butter</p>
<p>Olive oil</p>
<p>4 onions</p>
<p>4 10.5 oz cans of beef broth</p>
<p>1 teaspoon of dried thyme</p>
<p>Salt and pepper</p>
<p>Slices of Swiss and provolone cheese</p>
<p>1/4 cup of grated parmesan</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Melt the butter in a pot with a splash of olive oil. Add
  onions and stir until they are translucent
</p>
<p>
  <strong>Step 2: </strong> Add beef broth and thyme to soup and simmer for 30 minutes.
  Add salt and pepper to taste
</p>
<p>
  <strong>Step 3:</strong> Ladle the soup into oven safe bowl. Gently place a slice
  a of bread overtop the soup. Next layer a slice of provolone on top, then Swiss,
  then a tablespoon of parmesan cheese.
</p>
<p>
  <strong>Step 4:</strong> Broil the bowls in the oven until the cheese is bubbling
</p>
