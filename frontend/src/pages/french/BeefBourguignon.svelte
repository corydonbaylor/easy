<h1>Beef Bourguignon</h1>
<p>
  The first time I cooked dinner for my girlfriend&#39;s parents, I used this
  recipe. While there are a lot of ingredients to be found here, the actual
  cooking of the dish has very little hands on time. You will get something akin
  to Coq Au Vin but with a somewhat richer texture and taste. You can serve this
  with noodles or turn it into a gravy and serve with mashed potatoes.
</p>
<h2>Ingredients</h2>
<p>2 stalks of celery</p>
<p>1 yellow onion</p>
<p>minced garlic</p>
<p>1 lb of a fatty beef (stew beef or short ribs)</p>
<p>3 carrots</p>
<p>3 table spoons of tomato paste</p>
<p>2 cups of red wine</p>
<p>2 cups of chicken or beef stock</p>
<p>Parsley and thyme</p>
<p>Flour</p>
<p>Egg noodles</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Brown the beef in the bottom of your dutch oven. Set aside
  when golden brown
</p>
<p>
  <strong>Step 2:</strong> Dice onion and add to the dutch oven. Cook until translucent.
  Add tomato paste and a spoonful of flour
</p>
<p>
  <strong>Step 3:</strong> Add red wine and stock. Cut carrots into big thick pieces
  and add to the stew. Add parsley and thyme.
</p>
<p>
  <strong>Step 4:</strong> Cook for three hour on low heat until the meat is fork
  tender (the broth should be at about 200 degrees for the duration of the cooking
  process)
</p>
<p>
  <strong>Step 5:</strong> When the stew is 10 minutes out, cook noodles. Stir in
  chopped parsley with the noodles right before plating for extra color
</p>
<p>
  <strong>Step 5:</strong> When the beef is just about ready, cook the mushrooms
  in butter and garlic
</p>
<p>
  <strong>Step 6:</strong> Add the mushrooms to the stew. If the stew is too thin,
  add more flour. Take out the spent parsley and thyme.
</p>
<p>
  <strong>Step 7:</strong> If you would like the stew to become a gravy, strain out
  all the ingredients with a colander. On a frying pan, add a pad of butter. Wait
  until it melts and add some flour. Pour in the broth and mix with a spatula
</p>
