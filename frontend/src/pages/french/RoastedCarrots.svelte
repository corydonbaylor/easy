<h1>Roasted Carrots</h1>
<p>
  Carrots taste awful raw. They are tough, overly fibrous, and basically just a
  vessel to serve greater foods and textures like ranch and hummus. But once
  cooked, they undergo the most delicious transformation. They become soft and
  sweet and filing. They grow into their own. Properly spiced and seasoned,
  carrots can be the main attraction.
</p>
<h2>Ingredients</h2>
<p>Carrots (real carrots, not baby carrots)</p>
<p>Olive Oil</p>
<p>Lemon</p>
<p>Rosemary, thyme, sage or bay</p>
<p>Cloves of Garlic</p>
<h2>Instructions</h2>
<p><strong>Step 1:</strong> Cut carrots into large chunks</p>
<p>
  <strong>Step 2:</strong> Add carrots to a pot of boiling water seasoned with salt,
  cook until you are just able to pierce them with a fork but not until fully cooked
</p>
<p>
  <strong>Step 3:</strong> Dice herbs and smash garlic with wide side of a chef&#39;s
  nice. Set oven to 425 degrees
</p>
<p>
  <strong>Step 4:</strong> Add the cooked carrots to a bowl. Toss with salt, olive
  oil, a squeeze of lemon, garlic, and herbs
</p>
<p><strong>Step 5:</strong> Roast in oven for around half an hour</p>
