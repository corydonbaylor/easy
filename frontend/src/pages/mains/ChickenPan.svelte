<h1>Chicken with Pan Sauce</h1>
<p>
  The trick to eating right is to make some healthy something tasty. Bonus
  points if you get to use fancy French words like fond when making it. This
  will make the perfect main for a weeknight meal, especially one where you have
  about 40 mins to make dinner. You should have plenty of time to make sides
  when its in the oven, which typically takes 25-30 mins.
</p>
<p>
  The amount of ingredients is left purposefully vague because pan sauces are
  something you feel out more than you make with precision.
</p>
<h2>Ingredients</h2>
<p>
  2 chicken breasts bone in and skin on (it remains MUCH more moist this way)
</p>
<p>1 shallot</p>
<p>White Wine/Sherry</p>
<p>Chicken Stock</p>
<p>Butter</p>
<p>Teaspoon of flour</p>
<p>Thyme, Sage, Parsley</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Sear the chicken in a cast iron pan with some canola oil
  in the bottom of the pan. If the chicken is sticking to the pan, keep waiting,
  when the sear is complete it will form a brown crust and release from the pan--
  empty the grease, but don&#39;t clean the pan.
</p>
<p>
  <strong>Step 2:</strong> Place the chicken in a 375 degree oven until the internal
  temperature is 160 degrees.
</p>
<p>
  <strong>Step 3:</strong> While the chicken is cooking, chop up the shallot and
  spices. After the chicken is done cooking and is resting, you will make the pan
  sauce
</p>
<p>
  <strong>Step 4:</strong> Cook the shallots until they are translucent (you can
  add a little bit of flour, which can make your sauce thicker but its not necessary)
</p>
<p>
  <strong>Step 5:</strong> Add equal parts wine and chicken stock to the pan. Add
  in the spices. Reduce until the mixture coats the back of the spoon.
</p>
<p>
  <strong>Step 6:</strong> Add butter to the sauce and stir until it emulsifies
</p>
