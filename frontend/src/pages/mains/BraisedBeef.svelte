<h1>Braised Beef with Sunday Gravy</h1>
<p>
  Growing up, my mother would always make a tomatoey beef stew with carrots,
  celery, and big chunks of potato. I loved it. After traveling to Budapest, I
  found that it bears an uncanny resemblance to the flavor profiles of goulash.
  This is my take on my mothers dish, served over mashed potatoes and topped
  with pickled onions.
</p>
<h2>Ingredients</h2>
<p>1 package of beef for stews</p>
<p>1 can of tomato sauce</p>
<p>2 packages of beef stock</p>
<p>1 white or yellow onion</p>
<p>1 red or pickled onions</p>
<p>Vinger</p>
<p>Cornstarch</p>
<p>2 potatoes</p>
<p>Bay leaves</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1: </strong> Add one package of beef stock to a large pot; heat until
  it boils and then let it simmer
</p>
<p>
  <strong>Step 2:</strong> Cut the white onion in half and cut off the ends and add
  to pot
</p>
<p><strong>Step 3: </strong> Add beef to pot</p>
<p><strong>Step 4:</strong> Add 1/3 a cup of tomato sauce to pot</p>
<p>
  <strong>Step 4.5:</strong> If you don&#39;t have them, make pickled onions. Boil
  equal parts vinegar and water and once boiling, fill a jar with sliced onions (cut
  root to stem) and the mixture. It takes a few hours to pickle.
</p>
<p>
  <strong>Step 5:</strong> Cook for 4 hours; stirring occasionally and adding beef
  stock so that the beef is always submerged. Whenever you add stock, add in a spoonful
  of tomato sauce as well. The further along it gets, the less submerged you want
  it. By the end, it will turn into a gravy.
</p>
<p>
  <strong>Step 6:</strong> When you are about 45 mins out, stop adding stock. You
  will likely have used another half of a container at this point
</p>
<p>
  <strong>Step 7:</strong> When you are 30 mins out, add potatoes to boiling water
  and cook until fork tender (about 20 mins)
</p>
<p>
  <strong>Step 8:</strong> Take out potatoes and add a slab of butter and a reasonable
  amount of milk (like 1/3 a cup; just eye ball it). Add pepper and salt to taste
  and mash.
</p>
<p>
  <strong>Step 9:</strong> Mix a spoonful of cornstarch with a half mug of water.
  Make sure it is mixed well and then add it to the pot by streaming it in and stirring
  the mixture as you go. This should add some thickness to your gravy.
</p>
<p>
  <strong>Step 10:</strong> Assemble! Put the potatoes down first, then add the beef
  and gravy, then top with pickled onions.
</p>
