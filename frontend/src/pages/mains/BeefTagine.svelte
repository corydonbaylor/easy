<h1>Beef Tajine</h1>
<p>
  Tajine is a traditional Moroccan dish named after the earthenware pot in which
  its cooked. The pot itself is distinctive for its conical lid, which traps the
  moisture in the dish. However, for our meal, any old pot and lid will work.
</p>
<p>
  Our Tajine will be comprised of beef, potatoes, peas, and carrots. It is
  wonderful on a cold day or a day when you just need some comfort food. For
  desert, I would recommend greek yogurt with honey. If you are wondering what a
  Tajine is:
</p>
<blockquote>
  <p>
    The distinctive shape of the tagine is not only decorative but has an
    important function. Indeed, in an arid region like the Maghreb, it meets a
    local need to cook food without a lot of water. It also allows to cook
    without much fat, which keeps most of the natural flavor of food.
  </p>
  <p>-- 196flavors.com</p>
</blockquote>
<p>
  This is very easy to cook and comes together in under 45 minutes. Creates
  around 4 meals.
</p>
<h2>Ingredients</h2>
<p><strong>Spices</strong></p>
<p>2 teaspoons paprika</p>
<p>2 teaspoons cumin</p>
<p>1/4 teaspoon cayenne pepper</p>
<p>1 teaspoon ground ginger</p>
<p>1 teaspoon ground coriander</p>
<p>1 teaspoon cinnamon</p>
<p>1/2 teaspoon nutmeg</p>
<p><strong>Meat</strong></p>
<p>Beef cut into chunks</p>
<p><strong>Veggies</strong></p>
<p>1 yellow onion cut into slices</p>
<p>2 potatoes cut into cubes</p>
<p>2 large carrots cut into circles</p>
<p>1 can of peas</p>
<p><strong>Other</strong></p>
<p>Cilantro</p>
<p>Honey</p>
<p>1 3/4 Cups of Beef Stock</p>
<p>Minced Garlic</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Combine all the spices into a mix in a little mixing bowl
</p>
<p>
  <strong>Step 2:</strong> Cut the vegetables. Cubes for the potatoes and circles
  for the carrots.
</p>
<p>
  <strong>Step 3: </strong> Cook beef in large pan. You are just going to brown the
  beef rather than cook into completely through. Just sear it. It will spend around
  20 mins in the stew.
</p>
<p>
  <strong>Step 4:</strong> Cook onions in the same pan. Take out the beef and cook
  onions in the fat left from the beef.
</p>
<p>
  <strong>Step 5:</strong> Add <em>half</em> the spices in with the onion. It should
  form a bit of a sludge with the onions. Add in the garlic as well.
</p>
<p><strong>Step 6:</strong> Add beef stock, beef, and cut vegetables</p>
<p>
  <strong>Step 7:</strong> Add the other half of the spices and pour in about 2 teaspoons
  of honey
</p>
<p>
  <strong>Step 8:</strong> On medium/high heat, cover the pan, and let the vegetables
  steam and become soft. This will take around 20 mins
</p>
<p><strong>Step 9:</strong> Garnish with cut cilantro</p>
