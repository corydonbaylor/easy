<h1>Homemade Pasta</h1>
<p>
  Homemade pasta is a delight. The noodles always taste just a little more
  substantial when they are freshly made, like they are a focus of the meal
  rather than a vessel for the sauce. While the noodles could be easily paired
  with any number of sauces, I would recommend something light, because again,
  the focus should be on the noodles.
</p>
<p>
  I like to serve homemade pasta with pesto mixed with crushed walnuts, halved
  grape tomatoes, and loads of parmesan. Half the fun of this recipe is kneading
  the dough, making this recipe a good date night activity.
</p>
<p>In total, this takes about 1 hour and produces about 4 meals.</p>
<h2>Ingredients</h2>
<p><strong>Pasta</strong></p>
<p>2 cups of flour</p>
<p>8 eggs</p>
<p><strong>Pesto</strong></p>
<p>Store bought pesto</p>
<p>Cherry tomatoes</p>
<p>Walnuts</p>
<p>Parmesan</p>
<p><strong>Special Equipment</strong></p>
<p>Pasta maker</p>
<p>Plastic wrap</p>
<h2>Instructions</h2>
<p>
  <strong>Step 1:</strong> Separate 6 egg yolks and combine them with 2 additional
  eggs in a mixing bowl. Beat all 8 eggs together until well mixed
</p>
<p>
  <strong>Step 2:</strong> Pour the flour into a large bowl. You are going to create
  a little well with the flour. Essentially you want to be able to pour in your eggs
  into the center of the flour without the eggs seeping out.
</p>
<p>
  <strong>Step 3:</strong> Go ahead and pour your egg mixture into the center of
  the flour well. Using a fork, gradually incorporate more and more of the flour
  into the egg mixture. Keep whisking the egg while you are slowly incorporating
  the flour. Eventually this will form into a dough.
</p>
<p>
  <strong>Step 4:</strong> Once the dough becomes dough, begin kneading it for 7
  minutes. To knead, first sprinkle down some flour on your counter and fold and
  roll the dough.
</p>
<p>
  <strong>Step 5:</strong> After about 7 minutes of kneading, check to see if you
  dough is ready by poking it. If it bounces back, than the gluten has successfully
  developed
</p>
<p>
  <strong>Step 6:</strong> Wrap your dough in plastic wrap and let is rest for 30
  minutes. While the dough is resting, cut the cherry tomatoes in half, and crush
  the walnuts in ziplock bag with a rolling pen.
</p>
<p>
  <strong>Step 7:</strong> Boil the water. You will need a big pot with a lot of
  boiling water for this, so I would recommend getting that started when you are
  about half way done with cutting your pasta (which is the next step).
</p>
<p>
  <strong>Step 8:</strong> At this point, dough is ready to be cut into pasta. Use
  a roller to flatten the dough and then run it through the pasta maker. The pasta
  will expand once cooked so you want it to be as thin as possible.
</p>
<p>
  <strong>Step 9:</strong> Cook the pasta for about 5 minutes. Once the noodles start
  floating to the top, they are done. Pour it all out into a big bowl.
</p>
<p>
  <strong>Step 10: </strong> Combine the pasta, pesto, parmesan, cut tomatoes and
  walnuts all together and enjoy.
</p>
